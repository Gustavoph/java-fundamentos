package Outros;

/*
* Camel Case: lastName
* - pacotes
* - atributos
* - métodos
* - variáveis e parâmetros
*
* Pascal Case: ProductService
* - classes
*/

public class NomeDeVariaveis {
  public static void main(String[] args) {
    // int 5minutos; - não pode iniciar com numero
    int _5minutos; // caso precide do numero usa o _ para iniciar

    // int salário; - não pode usar acentuação em nome de variável
    int salario;

    // int salario do funcionario; - não pode ter espaços entre o nome
    int salarioDoFuncionario; // use o padrão camelCase
  }
}
