package EstruturaCondicional;

public class ExpressoesComparativas {
  public static void main(String[] args) {
    int x = 10;
    int y = 5;

    System.out.printf("X é igual a Y: %b %n", x == y);
    System.out.printf("X é maior a Y: %b %n", x > y);
    System.out.printf("X é maior ou igual a Y: %b %n", x >= y);
    System.out.printf("X é menor a Y: %b %n", x < y);
    System.out.printf("X é menor ou igual a Y: %b %n", x <= y);
    System.out.printf("X é diferente a Y: %b %n", x != y);
  }
}
